    private void btnUnesiPrisustvoActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnUnesiPrisustvoActionPerformed
        
        try {
            
            SimpleDateFormat sdf = new SimpleDateFormat("dd.MM.yyyy.");
            Date datum = sdf.parse(txtDatumPrisustvovanja.getText().trim());
            
            Clan clan = (Clan) cmbClanPrisustva.getSelectedItem();
            int index = cmbClanPrisustva.getSelectedIndex();
            
            Prisustvo prisustvo = new Prisustvo(0, datum, clan.getClanID());
            
            Controller.getInstance().addPrisustvo(prisustvo);
            
            popuniClanove(cmbClanPrisustva);
            cmbClanPrisustva.setSelectedIndex(index);
            
            JOptionPane.showMessageDialog(this, "Uspešno uneto prisustvo člana!");
            
        } catch (Exception e) {
            JOptionPane.showMessageDialog(this, e.getMessage());
            //e.printStackTrace();
        }
    }//GEN-LAST:event_btnUnesiPrisustvoActionPerformed

