    // PRISUSTVO - PRETRAZI (PO MESECU I GODINI)
    private void btnPretraziPrisustvoActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnPretraziPrisustvoActionPerformed
        try {
            
            Clan c = (Clan) cmbClanPrisustva.getSelectedItem();
            int mesec = cmbMesecPretraga.getSelectedIndex();
            mesec += 1;
            
            SimpleDateFormat sdf = new SimpleDateFormat("dd.MM.yyyy.");
            String datumStr = "01." + mesec + "." + txtGodinaPretraga.getText().trim() + ".";
            Date datum = sdf.parse(datumStr);
            
            Prisustvo prisustvo = new Prisustvo(0, datum, c.getClanID());
            
            List< Prisustvo> prisustva = Controller.getInstance().getAllByCriteriaPrisustvoClanaPoDatumu(prisustvo);
            
            ListModelPrisustva lm = (ListModelPrisustva) listPrisustva.getModel();
            lm.setPrisustva(prisustva);
            
            JOptionPane.showMessageDialog(this, "Uspešno pretražena prisustva člana!");
            
        } catch (Exception e) {
            JOptionPane.showMessageDialog(this, e.getMessage());
            //e.printStackTrace();
        }
    }//GEN-LAST:event_btnPretraziPrisustvoActionPerformed

