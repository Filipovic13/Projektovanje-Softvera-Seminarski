    private void popuniClanove(JComboBox cmb) {
        
        try {
            List<Clan> clanovi = Controller.getInstance().getAllClan();
            
            cmb.removeAllItems();
            
            for (Clan clan : clanovi) {
                cmb.addItem(clan);
            }
        } catch (Exception ex) {
            JOptionPane.showMessageDialog(this, ex.getMessage());
            ex.printStackTrace();
        }
    }
    
